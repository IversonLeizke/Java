public class Calcular {
    
    public static class somar {

        public static double operacao(double v1, double v2) {
        return v1 + v2;
        }
    }
    public static class aubtrair {

        public static double operacao(double v1, double v2) {
            return v1 - v2;
        }
    }
    public static class dividir {

        public static double operacao(double v1, double v2) {
            return v1 / v2;
        }
    }
    public static class multiplicar {

        public static double operacao(double v1, double v2) {
            return v1 * v2;
        }
    }
}
